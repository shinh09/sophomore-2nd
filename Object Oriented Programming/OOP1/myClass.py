class MyClass:

    def __init__(self, num1, num2):
        self.var_1 = num1
        self.var_2 = num2

    def add(self):
        return self.var_1 + self.var_2

    def minus(self):
        return self.var_1 - self.var_2

    def multiply(self):
        return self.var_1 * self.var_2

    def divide(self):
        
        return self.var_1 / self.var_2


        